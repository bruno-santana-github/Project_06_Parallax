        <script>
            $(function(){
                $('form').submit(function(){
                    var erro = false;
                    var nome = $('[name=nome]').val();
                    var email = $('[name=email]').val();
                    var telefone = $('[name=telefone]').val();
                    var mensagem = $('[name=mensagem]').val();
                    resetForm();
                    if(nome == ''){
                        erro = true;
                        var el = $('[name=nome]');
                        el.parent().find('span').html('Por favor insira seu nome.');
                        el.css('border','2px solid red');
                    }
                    if(email == ''){
                        erro = true;
                        var el = $('[name=email]');
                        el.parent().find('span').html('Por favor insira seu e-mail.');
                        el.css('border','2px solid red');
                    }
                    if(telefone == ''){
                        erro = true;
                        var el = $('[name=telefone]');
                        el.parent().find('span').html('Por favor insira seu telefone.');
                        el.css('border','2px solid red');
                    }
                    if(mensagem == ''){
                        erro = true;
                        var el = $('[name=mensagem]');
                        el.parent().find('span').html('Por favor insira seu mensagem.');
                        el.css('border','2px solid red');
                    }

                    if(erro == false){
                        $('.box-sucesso').fadeIn(function(){
                            setTimeout(function(){
                                $('.box-sucesso').fadeOut();
                            },5000);
                        });
                        $(this)[0].reset();
                    }

                    return false;
                })

                function resetForm(){
                    $('input[type=text],input[type=email],textarea').css('border','1px solid #ccc');
                    $('span').html('');
                }
            })
        </script>
